                                <p>In 1829, to determine whether locomotive traction traction was best for the carriage of passengers and freight on their line, the directors of the Liverpool and Manchester Railway offered a prize of £500 to the designers of a locomotive which did not exceed six tons in weight, but was able to pull a load of at least three times that, at a minimum of ten miles an hour. At the Rainhill Trials in 1829, Robert Stephenson’s Rocket reached thirty miles an hour, completely outclassing the competition and marking the beginning of the Steam Age.</p>
                                <p>The building of railway lines proceeded apace in the 1830s. The line between Lancashire and London, via Birmingham, the country’s most important artery was completed to Birmingham in 1837 and from there to London the following year. The fine Doric arch erected outside the London terminal at Euston at a cost of £35,000 emphasised the importance of the line. In 1839 and 1840 it was linked by other lines to Derby, Leicester, Nottingham, Leeds, Chester and York. From London trunk lines began to radiate outwards, reaching Southampton in 1840, Bristol in 1841, Dover and Exeter via Bristol in 1844 and Yarmouth via Norwich in 1845. Railways timetables began to be issued by George Bradshaw at the end of the 1830s and the first W. H. Smith’s bookstall appeared in 1848.</p>
